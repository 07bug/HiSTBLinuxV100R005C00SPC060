HI_S32 HI_CA_GetRSAKeyInfor(AUTHALG_E eAuthType, HI_UNF_CIPHER_RSA_PUB_KEY_S *pstPubKey)
{
    if (pstPubKey == HI_NULL)
    {
       return HI_FAILURE;
    }

     pstPubKey->pu8N = g_customer_rsa_public_key_N;
     pstPubKey->u16NLen = sizeof(g_customer_rsa_public_key_N);
     if (g_customer_rsa_public_key_E == 0x3)
     {
         pstPubKey->pu8E = g_customer_rsa_public_key_E_3;
     }
     else if (g_customer_rsa_public_key_E == 0x100001)
     {
         pstPubKey->pu8E = g_customer_rsa_public_key_E_100001;
     }
     else if (g_customer_rsa_public_key_E == 0x10001)
     {
         pstPubKey->pu8E = g_customer_rsa_public_key_E_10001;
     }
     else
     {
        return HI_FAILURE;
     }
     
     pstPubKey->u16ELen = 256;    

    return HI_SUCCESS;
}