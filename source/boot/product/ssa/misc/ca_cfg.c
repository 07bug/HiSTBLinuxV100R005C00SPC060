HI_S32 HI_CA_GetKeyladderKey(HI_CA_KEY_GROUP_E enKeyGroup, HI_U8 u8KeyladderKey1[16], HI_U8 u8KeyladderKey2[16], HI_U8 u8CipherKey[16])
{
    HI_U8 *pu8KeyLadderkey1, *pu8KeyLadderkey2, *pu8CipherKey;
    
    if (enKeyGroup == HI_CA_KEY_GROUP_1)
    {
       pu8KeyLadderkey1 = G1_KEYLADDER_KEY1;
       pu8KeyLadderkey2 = G1_KEYLADDER_KEY2;
       pu8CipherKey = G1_CIPHERKEY;
    }
    else if (enKeyGroup == HI_CA_KEY_GROUP_2)
    {
       pu8KeyLadderkey1 = G2_KEYLADDER_KEY1;
       pu8KeyLadderkey2 = G2_KEYLADDER_KEY2;
       pu8CipherKey = G2_CIPHERKEY;
    }
    else
    {
       return HI_FAILURE;
    }
    
    if (u8KeyladderKey1 != HI_NULL)
    {
        memcpy(u8KeyladderKey1, pu8KeyLadderkey1, KEY_LEN);
    }
    
    if (u8KeyladderKey2 != HI_NULL)
    {
        memcpy(u8KeyladderKey2, pu8KeyLadderkey2, KEY_LEN);
    }
    
    if (u8CipherKey != HI_NULL)
    {
        memcpy(u8CipherKey, pu8CipherKey, KEY_LEN);
    }    

    return HI_SUCCESS;
}

