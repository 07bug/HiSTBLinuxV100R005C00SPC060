HI_S32 BLSH_HashVerify(BLSH_HASH_TYPE_E enHashType, HI_U8* pu8Data, HI_U32 u32Size, HI_U8* pu8Value)
{
    HI_U8 au8HashValue[BLSH_LEN_HMAXVALUE] = {0};
    HI_UNF_CIPHER_HASH_ATTS_S stHashAttr;
    HI_HANDLE hHashHandle = HI_NULL_PTR;
    HI_S32 s32Ret = HI_FAILURE;

    LOADER_FuncEnter();

    LOADER_CHECK_PARAM(HI_NULL_PTR == pu8Data);
    LOADER_CHECK_PARAM(HI_NULL_PTR == pu8Value);
    LOADER_CHECK_PARAM(0 == u32Size);

    LOADER_MemSet(&stHashAttr, 0x00, sizeof(stHashAttr));

    switch (enHashType)
    {
        case BLSH_HASH_TYPE_1:
        {
            stHashAttr.eShaType = HI_UNF_CIPHER_HASH_TYPE_SHA1;
            break;
        }
        case BLSH_HASH_TYPE_2:
        {
            stHashAttr.eShaType = HI_UNF_CIPHER_HASH_TYPE_SHA256;
            break;
        }
        default:
        {
            LOADER_PrintErrCode(LOADER_ERR_PARAM);
            return LOADER_ERR_PARAM;
        }
    }

#ifdef HI_LOADER_BOOTLOADER
    stHashAttr.u32TotalDataLen = u32Size;
#endif

    s32Ret = HI_UNF_CIPHER_HashInit(&stHashAttr, &hHashHandle);
    if (HI_SUCCESS != s32Ret)
    {
        LOADER_PrintFuncErr(HI_UNF_CIPHER_HashInit, s32Ret);
        return LOADER_ERR_EXTERNAL;
    }

    s32Ret = HI_UNF_CIPHER_HashUpdate(hHashHandle, pu8Data, u32Size);
    if (HI_SUCCESS != s32Ret)
    {
        LOADER_PrintFuncErr(HI_UNF_CIPHER_HashUpdate, s32Ret);
        return LOADER_ERR_EXTERNAL;
    }

    s32Ret = HI_UNF_CIPHER_HashFinal(hHashHandle, au8HashValue);
    if (HI_SUCCESS != s32Ret)
    {
        LOADER_PrintFuncErr(HI_UNF_CIPHER_HashFinal, s32Ret);
        return LOADER_ERR_EXTERNAL;
    }

    LOADER_PrintBuffer("ImageHashValue:", pu8Value, BLSH_LEN_HMAXVALUE);
    LOADER_PrintBuffer("CalcuHashValue:", au8HashValue, BLSH_LEN_HMAXVALUE);

    if (0 != LOADER_MemCmp(au8HashValue, pu8Value, BLSH_LEN_HMAXVALUE))
    {
        LOADER_PrintErrCode(LOADER_ERR_DATABASE);
        return LOADER_ERR_DATABASE;
    }

    LOADER_FuncExit();
    return HI_SUCCESS;
}

HI_S32 BLSH_RSAVerify(HI_U8* pu8Data, HI_U32 u32Size, HI_U8* pu8Signature, HI_U32 u32SignatureSize)
{
    HI_UNF_CIPHER_RSA_VERIFY_S stRsaVerify;
    HI_S32 s32Ret = HI_FAILURE;

    LOADER_FuncEnter();

    LOADER_CHECK_PARAM(HI_NULL_PTR == pu8Data);
    LOADER_CHECK_PARAM(HI_NULL_PTR == pu8Signature);

    LOADER_CHECK_PARAM(0 == u32Size);
    LOADER_CHECK_PARAM(0 == u32SignatureSize);

    LOADER_MemSet(&stRsaVerify, 0x00, sizeof(stRsaVerify));

    s32Ret = blshGetRSAVerifyParam(&stRsaVerify);
    if (HI_SUCCESS != s32Ret)
    {
        LOADER_PrintFuncErr(blshGetRSAVerifyParam, s32Ret);
        return LOADER_ERR_EXTERNAL;
    }

    s32Ret = HI_UNF_CIPHER_RsaVerify(&stRsaVerify, pu8Data, u32Size, HI_NULL_PTR, pu8Signature, u32SignatureSize);
    if (HI_SUCCESS != s32Ret)
    {
        LOADER_PrintFuncErr(HI_UNF_CIPHER_RsaVerify, s32Ret);
        return LOADER_ERR_EXTERNAL;
    }

    LOADER_FuncExit();
    return HI_SUCCESS;
}


HI_S32 BLSH_SetRSAPublicKey(HI_U8* pu8RsaData, HI_U32 u32Length)
{
    LOADER_FuncEnter();

    LOADER_CHECK_PARAM(HI_NULL_PTR == pu8RsaData);
    LOADER_CHECK_PARAM(BLSH_LEN_RSAPUBLICKEY > u32Length);

    LOADER_MemCpy(g_au8CustomerRSAPublicKey_N, pu8RsaData, BLSH_LEN_RSA_PUBLIC_KEY_N);

    s_s32CustomerRSAPublicKey_E = (pu8RsaData[BLSH_POS_RSA_PUBLIC_KEY_E] << 24)
                                + (pu8RsaData[BLSH_POS_RSA_PUBLIC_KEY_E + 1] << 16)
                                + (pu8RsaData[BLSH_POS_RSA_PUBLIC_KEY_E + 2] << 8)
                                + (pu8RsaData[BLSH_POS_RSA_PUBLIC_KEY_E + 3]);
    LOADER_FuncExit();
    return HI_SUCCESS;
}

HI_S32 BLSH_ConfigExternKey(LOADER_FLASH_OPENPARAM_S* pstOpenParam, HI_U8* pu8Buffer, HI_U32 u32Size, HI_U64 u64ReadAddress)
{
    HI_U32 u32DataSize = 0;
    HI_HANDLE hFlashHandle = HI_INVALID_HANDLE;

    HI_S32 s32Ret = HI_FAILURE;

    LOADER_FuncEnter();

    LOADER_CHECK_PARAM(HI_NULL_PTR == pstOpenParam);
    LOADER_CHECK_PARAM(HI_NULL_PTR == pu8Buffer);
    LOADER_CHECK_PARAM(BLSH_LEN_RSAPUBLICKEY > u32Size);

    hFlashHandle = LOADER_FLASH_Open(pstOpenParam);
    if (HI_INVALID_HANDLE == hFlashHandle)
    {
        LOADER_PrintFuncErr(LOADER_FLASH_Open, LOADER_ERR_FLASH);
        return LOADER_ERR_FLASH;
    }

    u32DataSize = LOADER_FLASH_Read(hFlashHandle, u64ReadAddress, pu8Buffer, u32Size, BLSH_LEN_RSAPUBLICKEY);
    if (BLSH_LEN_RSAPUBLICKEY > u32DataSize)
    {
        LOADER_PrintFuncErr(LOADER_FLASH_Read, LOADER_ERR_FLASH);

        LOADER_FLASH_Close(hFlashHandle);
        hFlashHandle = HI_NULL_PTR;

        return LOADER_ERR_FLASH;
    }

    LOADER_FLASH_Close(hFlashHandle);
    hFlashHandle = HI_NULL_PTR;

    s32Ret = BLSH_SetRSAPublicKey(pu8Buffer, BLSH_LEN_RSAPUBLICKEY);
    if (HI_SUCCESS != s32Ret)
    {
        LOADER_PrintFuncErr(BLSH_SetRSAPublicKey, s32Ret);
        return s32Ret;
    }

    LOADER_FuncExit();
    return HI_SUCCESS;
}

